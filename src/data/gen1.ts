const SPECIAL_VARIANTS: Record<string, string[]> = {
  "Mr. Mime": ["mr mime", "mister mime", "mr. mime"],
  "Farfetch'd": ["farfetchd", "farfetch'd", "far fetched"],
  "Nidoran♀": ["nidoran female", "nidoran f", "nidoran girl", "nidoran♀"],
  "Nidoran♂": ["nidoran male", "nidoran m", "nidoran boy", "nidoran♂"],
};

export const GEN1_POKEMON: Gen1Pokemon[] = CANONICAL_NAMES.map((name, index) => {
  const normalized = new Set<string>([normalize(name)]);
  for (const variant of SPECIAL_VARIANTS[name] ?? []) {
    normalized.add(normalize(variant));
  }

  return {
    id: index + 1,
    name,
    variants: [...normalized],
  };
});
